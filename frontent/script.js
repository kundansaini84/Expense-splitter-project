let contract;

window.addEventListener("load", async () => {
    if (window.ethereum) {
        web3 = new Web3(window.ethereum);
        await window.ethereum.request({ method: "eth_requestAccounts" });
        const accounts = await web3.eth.getAccounts();
        contract = new web3.eth.Contract(contractABI, contractAddress);
    } else {
        alert("Please install MetaMask!");
    }
});

async function createExpense() {
    const desc = document.getElementById("expenseDesc").value;
    const amount = document.getElementById("expenseAmount").value;
    const participants = document.getElementById("participants").value.split(",");

    const accounts = await web3.eth.getAccounts();
    await contract.methods.createExpense(desc, amount, participants).send({ from: accounts[0] });
    alert("Expense created successfully!");
}

async function payShare() {
    const expenseId = document.getElementById("expenseId").value;
    const accounts = await web3.eth.getAccounts();
    const share = await contract.methods.checkBalance().call({ from: accounts[0] });

    await contract.methods.payShare(expenseId).send({ from: accounts[0], value: share });
    alert("Payment successful!");
}

async function checkBalance() {
    const accounts = await web3.eth.getAccounts();
    const balance = await contract.methods.checkBalance().call({ from: accounts[0] });
    document.getElementById("balanceDisplay").innerText = `Your balance: ${web3.utils.fromWei(balance, "ether")} ETH`;
}
